#!/usr/bin/env node

const { App } = require('@slack/bolt');
const { spawn } = require('child_process');
const fs = require('fs');
const path = require('path');

// Initialize Slack app
const app = new App({
  token: process.env.SLACK_BOT_TOKEN,
  signingSecret: process.env.SLACK_SIGNING_SECRET,
  socketMode: true,
  appToken: process.env.SLACK_APP_TOKEN,
});

const PROJECT_ROOT = path.dirname(__dirname);

// Listen for @PM-agent mentions in #build-bot channel
app.message(/Build Bot/i, async ({ message, say, client }) => {
  console.log('ğŸ”” PM-agent mentioned in Slack');
  
  // Check if in allowed channel (build-bot, build, bot, or any channel if BUILD_BOT_CHANNEL not set)
  const allowedChannel = process.env.BUILD_BOT_CHANNEL || 'build-bot';
  const channelInfo = await client.conversations.info({ channel: message.channel });
  
  // Allow if no restriction set, or if channel name contains the allowed pattern
  if (allowedChannel !== '*' && channelInfo.channel.name && !channelInfo.channel.name.includes(allowedChannel.replace('#', ''))) {
    console.log(`ğŸš« Ignoring mention in #${channelInfo.channel.name} (only responding in channels containing "${allowedChannel}")`);
    return;
  }

  // Extract requirements from message
  const requirements = message.text.replace(/<@[^>]+>/g, '').trim();
  
  if (!requirements) {
    await say({
      text: "ğŸ¤– PM-agent here! Please provide the full requirement text for me to generate a spec.",
      thread_ts: message.ts
    });
    return;
  }

  // React to show processing
  await client.reactions.add({
    channel: message.channel,
    timestamp: message.ts,
    name: 'robot_face'
  });

  try {
    console.log('ğŸ“‹ Generating spec from Slack message...');
    
    // Generate spec using AutoGen
    const specContent = await generateSpecFromRequirements(requirements);
    
    // Save spec.md
    const specPath = path.join(PROJECT_ROOT, 'spec.md');
    fs.writeFileSync(specPath, specContent);
    
    // Upload spec as file to Slack thread
    await client.files.upload({
      channels: message.channel,
      thread_ts: message.ts,
      file: fs.createReadStream(specPath),
      filename: 'spec.md',
      title: 'Generated Product Specification',
      initial_comment: `âœ… **Specification Generated Successfully!**

ğŸ“‹ **Next Steps:**
1. Review and edit the \`spec.md\` file as needed
2. Run: \`./devin/devin_run.sh --issue <issue-number> --open\`
3. Or run: \`./devin/devin_run.sh --spec "custom requirements"\`

ğŸ”§ **Quick Commands:**
â€¢ \`/devin-run --issue 1\` - Generate from GitHub issue
â€¢ \`/devin-run --spec\` - Generate from current spec.md
â€¢ \`/cost-check\` - Check current pipeline costs

Generated by AutoGen Build Bot + Scribe-agent`
    });

    // Update reaction to checkmark
    await client.reactions.remove({
      channel: message.channel,
      timestamp: message.ts,
      name: 'robot_face'
    });
    
    await client.reactions.add({
      channel: message.channel,
      timestamp: message.ts,
      name: 'white_check_mark'
    });

  } catch (error) {
    console.error('âŒ Error generating spec:', error);
    
    await say({
      text: `âŒ **Spec Generation Failed**\n\`\`\`\n${error.message}\n\`\`\`\n\nğŸ”§ **Troubleshooting:**\nâ€¢ Check if Ollama is running: \`ollama serve\`\nâ€¢ Verify phi3:mini model: \`ollama list\`\nâ€¢ Check pipeline status: \`./scripts/monitor_costs.sh\``,
      thread_ts: message.ts
    });

    // Update reaction to X
    await client.reactions.remove({
      channel: message.channel,
      timestamp: message.ts,
      name: 'robot_face'
    });
    
    await client.reactions.add({
      channel: message.channel,
      timestamp: message.ts,
      name: 'x'
    });
  }
});

// Slash command: /devin-run
app.command('/devin-run', async ({ command, ack, respond, client }) => {
  await ack();
  
  const args = command.text.trim().split(/\s+/);
  let issueId = '';
  let useSpec = false;
  
  // Parse arguments
  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--issue' && args[i + 1]) {
      issueId = args[i + 1];
      i++;
    } else if (args[i] === '--spec') {
      useSpec = true;
    }
  }
  
  if (!issueId && !useSpec) {
    await respond({
      text: `âŒ **Invalid Usage**\n\nUsage:\nâ€¢ \`/devin-run --issue <number>\` - Generate from GitHub issue\nâ€¢ \`/devin-run --spec\` - Generate from current spec.md\n\nExample: \`/devin-run --issue 1\``
    });
    return;
  }

  await respond({
    text: `ğŸš€ **Devin Pipeline Starting...**\n\n${issueId ? `ğŸ“‹ Issue: #${issueId}` : 'ğŸ“„ Using: spec.md'}\n\nâ³ This will take ~15 minutes. I'll update you when complete.`
  });

  try {
    // Build devin command
    const devinCmd = ['./devin/devin_run.sh'];
    if (issueId) {
      devinCmd.push('--issue', issueId);
    }
    devinCmd.push('--open');

    console.log('ğŸ¤– Running Devin:', devinCmd.join(' '));
    
    // Execute devin script
    const devin = spawn(devinCmd[0], devinCmd.slice(1), {
      cwd: PROJECT_ROOT,
      stdio: ['pipe', 'pipe', 'pipe']
    });

    let output = '';
    let errorOutput = '';

    devin.stdout.on('data', (data) => {
      output += data.toString();
      console.log(data.toString());
    });

    devin.stderr.on('data', (data) => {
      errorOutput += data.toString();
      console.error(data.toString());
    });

    devin.on('close', async (code) => {
      if (code === 0) {
        // Extract PR URL from output
        const prUrlMatch = output.match(/Pull request created: (https:\/\/[^\s]+)/);
        const branchMatch = output.match(/Branch: (devin\/[^\s]+)/);
        
        await client.chat.postMessage({
          channel: command.channel_id,
          text: `âœ… **Devin Completed Successfully!**\n\nğŸ¯ **Results:**\nâ€¢ Branch: \`${branchMatch ? branchMatch[1] : 'Unknown'}\`\nâ€¢ ${prUrlMatch ? `PR: ${prUrlMatch[1]}` : 'PR creation may have failed'}\n\nğŸ”„ **Next Steps:**\n1. Review the pull request\n2. Test locally: \`git checkout ${branchMatch ? branchMatch[1] : 'branch-name'} && npm run dev\`\n3. Run tests: \`npm test\`\n4. Approve and merge when ready\n\nğŸ’° **Cost Check:** Run \`/cost-check\` to see usage`
        });
      } else {
        await client.chat.postMessage({
          channel: command.channel_id,
          text: `âŒ **Devin Failed**\n\n\`\`\`\n${errorOutput || output}\n\`\`\`\n\nğŸ”§ **Troubleshooting:**\nâ€¢ Check pipeline status: \`./scripts/monitor_costs.sh\`\nâ€¢ Verify kill switch: \`echo $HALT_PIPELINE\`\nâ€¢ Check GitHub CLI: \`gh auth status\``
        });
      }
    });

  } catch (error) {
    console.error('âŒ Error running Devin:', error);
    
    await client.chat.postMessage({
      channel: command.channel_id,
      text: `âŒ **Devin Execution Failed**\n\n\`\`\`\n${error.message}\n\`\`\`\n\nPlease check the logs and try again.`
    });
  }
});

// Slash command: /cost-check
